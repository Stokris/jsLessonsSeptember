//shortcut--addition operator
x += 1; // same as saying x + 1
console.log(x);

//subtraction operator
x -= 1; //same as saying x - 1
console.log(x);

//multiplication operator
x *= 1; // x * 1
console.log(x);

//division operator
x /= 1; // x / 5
console.log(x);

console.log(10%3); // equals one, 3*3 equals 9 with a remainder of one, remainder is answer
console.log(x % 5);
x = x % 5; //% sign is "modulus", division sign
x %= 5; //the same as line 30

//exponential assignment
let x = 5;
x = x ** 2;
x **= 1;
